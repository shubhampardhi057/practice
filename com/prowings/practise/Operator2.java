package com.prowings.practise;

public class Operator2
{
	public static void main(String[] args)
	{
		int a=5;
		int b=10;
		
		System.out.println( a == b);
		System.out.println( a != b);
		System.out.println( a >  b);
		System.out.println( a <  b);
		System.out.println( a >= b);
		System.out.println( a <= b);
		
		
	}
	
	
}
