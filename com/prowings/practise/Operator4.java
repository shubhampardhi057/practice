package com.prowings.practise;

public class Operator4
{
	public static void main(String[] args)
	{
		int a=10;
		int b=20;
		
		
		
		
		//  prefix (++a,--b)
		
		System.out.println(++a);
		System.out.println(--a);
		
		//  postfix (a++,b--)
		
		System.out.println(a++);
		System.out.println(b--);
	}
	
	
}
